def mp3_player(path):
    # MP3播放器
    global killFlag, stopFlag
    # 定义数据流块
    CHUNK = 1024
    # 只读方式打开wav文件
    wf = wave.open(path, 'rb')
    p = pyaudio.PyAudio()
    # 打开数据流
    stream = p.open(format=p.get_format_from_width(wf.getsampwidth()),
                    channels=wf.getnchannels(),
                    rate=wf.getframerate(),
                    output=True)
    # 读取数据
    data = wf.readframes(CHUNK)
    # 去除开头的空值
    while data == music_stop:
        data = wf.readframes(CHUNK)
    # 播放
    while data is not b'' and not killFlag:
        stream.write(data)
        data = wf.readframes(CHUNK)
        while stopFlag:
            time.sleep(1)
            pass
    # 停止数据流
    stream.stop_stream()
    stream.close()
    # 关闭 PyAudio
    p.terminate()
    stopFlag = False
    killFlag = False
